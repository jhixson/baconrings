<h1>Frequently Asked Questions</h1>

<p>
<strong>Why did you create this site?</strong>
<br />The purpose of this site is to be a great resource for students, prospective students, and parents. We want to help them make more informed decisions about higher education. We would also like to give students a place to voice their opinions. Hey, these days a college education is expensive, you should be able to share your thoughts. 
</p>

<p>
<strong>Does my email address show up when I leave a rating?</strong>
<br />Nope, you are never personally identified when you leave a rating!
</p>

<p>
<strong>Do you ever share my personal information with any third parties?</strong>
<br />Take a look at our <a href="/pages/privacy-policy">privacy policy</a> to see how your information is being used.
</p>

<p>
<strong>How soon are my ratings posted?</strong>
<br />If your rating is not immediately posted, give us a day to review it and approve it.  Our Moderation Team will look things over to make sure it meets our <a href="/pages/site-guidelines">site guidelines</a>.
</p>

<p>
<strong>Who can rate? Is it just for students?</strong>
<br />Nope, the site is open to students, prospective students, parents, alumni, and faculty/staff. When you create an account, you will be able to tell us who you are.
</p>

<p>
<strong>What if I see an error or some missing info for one of the items in the site?</strong>
<br />Just click the "submit correction" button under the item's info. Fill out the form and tell us what to correct or add and we will take care of it.
</p>

<p>
<strong>Some of my comments got deleted. Why? What are your rater guidelines?</strong>
<br />We want this site to be a reliable resource for the college community. For this reason, we have specific <a href="/pages/site-guidelines">site guidelines</a> for submitting ratings.
</p>

<p>
<strong>How can I be my school's moderator?</strong>
<br />Just drop us a line by filling out the <a href="/forms/contact">contact form</a>. We welcome your help in keeping your school's listing accurate and up-to-date.
</p>

<p>
<strong>How exactly do the scores work? How are the numbers calculated?</strong>
<br />It's simple. Each attribute is rated on a scale of 1 to 5 (1 being the lowest end of the scale, and 5 being the highest). The number you see is simply the average of the ratings. The overall campus score is the average of ALL of the ratings for that campus. The overall rating for each category (dorms, dining halls, etc.) is the average of all ratings for that category.
</p>

<p>
<strong>I requested to add a new category or item to a school page, but its not showing up yet? How long does it take?</strong>
<br />Give us a day or two to verify the item you want listed, collect the information, and post it up on the site.
</p>

<p>
<strong>Yikes, I saw a very derogatory rating, how can I request that it be removed?</strong>
<br />Easy. Just click the "flag this" button next to the rating. Our Moderation Team will receive an email and review it. If the rating doesn't meet our <a href="/pages/site-guidelines">site guidelines</a>, it will be edited or removed.
</p>

